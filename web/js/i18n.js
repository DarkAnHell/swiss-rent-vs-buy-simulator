let currentLang = "en";

/**
 * Get a translation string by key.
 */
export function t(key) {
  return TRANSLATIONS[currentLang]?.[key] ?? TRANSLATIONS.en[key] ?? key;
}

/**
 * Get current language code.
 */
export function getLang() {
  return currentLang;
}

/**
 * Set the active language and update the DOM.
 */
export function setLang(lang) {
  if (!TRANSLATIONS[lang]) return;
  currentLang = lang;
  document.documentElement.lang = lang;
  applyTranslations();
}

/**
 * Apply translations to all elements with data-i18n attributes.
 */
export function applyTranslations() {
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.dataset.i18n;
    const text = t(key);
    if (text) el.textContent = text;
  });

  // Also translate param labels via data-param
  document.querySelectorAll(".param-row[data-param]").forEach((row) => {
    const param = row.dataset.param;
    const key = "param_" + param;
    const text = TRANSLATIONS[currentLang]?.[key];
    if (!text) return;
    const label = row.querySelector(".param-label");
    if (label) label.textContent = text;
    const checkLabel = row.querySelector(".param-checkbox label");
    if (checkLabel) checkLabel.textContent = text;
  });
}
